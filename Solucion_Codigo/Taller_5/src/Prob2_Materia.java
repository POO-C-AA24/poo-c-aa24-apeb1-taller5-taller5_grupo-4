
public class Prob2_Materia {
    public String nombre;
    public double acd;
    public double ape;
    public double aa;
    public Prob2_Materia(String nombre, double acd, double ape, double aa) {
        this.nombre = nombre;
        this.acd = acd;
        this.ape = ape;
        this.aa = aa;
    }
    public String getNombre() {
        return nombre;
    }
    public double getAcd() {
        return acd;
    }
    public double getApe() {
        return ape;
    }
    public double getAa() {
        return aa;
    }
}
